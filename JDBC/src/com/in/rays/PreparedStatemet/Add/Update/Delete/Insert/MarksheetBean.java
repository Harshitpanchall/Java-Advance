package com.in.rays.PreparedStatemet.Add.Update.Delete.Insert;

public class MarksheetBean {
	private int id;
	private String name;
	private int roll;
	private int maths;
	private int phy;
	private int hin;
	
	public int getId() {
		return id;
		
	}
	
	public void setId(int id) {
		this.id = id;
		
	}
	
	public String getName() {
		return name;
		
	}
	
	public void setName(String name) {
		this.name = name;
		
	}
	
	public int getRoll() {
		return roll;
		
	}
	
	public void setRoll(int roll) {
		this.roll = roll;
	}
	
	public int getMaths() {
		return maths;
		
	}
	
	public void setMaths(int maths) {
		this.maths = maths;
		
	}
	
	public int getPhy() {
		return phy;
		
	}
	
	public void setPhy(int phy) {
		this.phy = phy;
		
	}
	
	public int getHin() {
		return hin;
		
	}
	
	public void setHin(int hin) {
		this.hin = hin;
	}

}
